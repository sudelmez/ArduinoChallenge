int kirmizi=2;
int sari=3;
int yesil=4;

void setup()
{
  pinMode(kirmizi, OUTPUT);
  pinMode(sari, OUTPUT);
  pinMode(yesil, OUTPUT);

}

void loop()
{
  digitalWrite(kirmizi, HIGH);
  digitalWrite(sari, LOW);
  digitalWrite(yesil, LOW);
  delay(3000); 
  digitalWrite(sari, HIGH);
  delay(1000);
  digitalWrite(kirmizi, LOW);
  delay(2000);
  digitalWrite(sari,LOW);
  digitalWrite(yesil, HIGH);
  delay(3000);
}
